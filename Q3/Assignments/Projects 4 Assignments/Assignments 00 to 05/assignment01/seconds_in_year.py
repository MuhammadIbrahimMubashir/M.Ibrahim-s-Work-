# Constants
days_in_year = 365
hours_in_day = 24
minutes_in_hour = 60
seconds_in_minute = 60

# Calculate the total number of seconds in a year
seconds_in_year = days_in_year * hours_in_day * minutes_in_hour * seconds_in_minute

# Print the result
print(f"There are {seconds_in_year} seconds in a year!")
